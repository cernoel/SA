    public window_shop_table(List<Shop> shops) {
        if (shops.isEmpty()) {
            simple_dialog.infoBox("Keine Daten übergeben!");
            return;
        }
        initComponents();

        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

         for (Shop shop : shops) {
            Object rowData[] = {
                Long.toString(shop.getId()),
                Float.toString(shop.getLon()),
                Float.toString(shop.getLat()),
                shop.getShop(),
                shop.getCity(),
                shop.getName(),
                shop.getPostcode(),
                shop.getStreet(),
                shop.getHousenumber(),
                shop.getWebsite(),
                shop.getEmail(),
                shop.getPhone()
                 };
            model.addRow(rowData);
        }
        tblResult.setModel(model);
        this.setLocationRelativeTo(null); // sets windows into the midst of the screen
    }

