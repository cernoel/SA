    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        
        if(simple_dialog.yesNoBox("Shop " + tblResult.getValueAt(tblResult.getSelectedRow(), 0).toString() + " wirklich löschen?") == false)
        {
            simple_dialog.infoBox("Löschen abgebrochen!");
            return;
        }
        
        DB_Access db = null;
        try {
            db = new DB_Access();
        } catch (SQLException ex) {
            simple_dialog.infoBox("Problem mit Datenbankverbindung!");
            return;
        }

        try {
            db.deleteShop(tblResult.getValueAt(tblResult.getSelectedRow(), 0).toString());
        } catch (SQLException ex) {
            System.out.println("Fehler löschen aus Datenbank: " + ex.getMessage());
            return;
        }
        
        simple_dialog.infoBox("Ausgewählten Shop gelöscht! Bitte fenster erneuern!");
    }//GEN-LAST:event_btnDeleteMouseClicked

