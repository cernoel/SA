    public window_shop_edit(String shopid) {
        initComponents();
        if (shopid.equals("0")) {
            newShop = true;
        }

        if (newShop) {
            txtId.setText("0");
            txtLat.setText("");
            txtLon.setText("");
            txtType.setText("");
            txtName.setText("");
            txtCity.setText("");
            txtStreet.setText("");
            txtHousenumber.setText("");
            txtPostcode.setText("");
            txtEmail.setText("");
            txtWebsite.setText("");
            txtPhone.setText("");
            return;
        }

        try {
            dba = new DB_Access();
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }

        List<Shop> shops = new ArrayList<>();
        
        try {
            shops = dba.filterShops(shopid);
        } catch (SQLException ex) {
            System.out.println("Error in Statement" + ex.getMessage());
        }

        if (shops.size() > 1) {
            simple_dialog.infoBox("Zu viele Ergebnisse!");
            return;
        }

        for (Shop shop : shops) {
            lShopId = shop.getId();
            txtId.setText(Long.toString(shop.getId()));
            txtLat.setText(Float.toString(shop.getLat()));
            txtLon.setText(Float.toString(shop.getLon()));
            txtType.setText(shop.getShop());
            txtName.setText(shop.getName());
            txtCity.setText(shop.getCity());
            txtStreet.setText(shop.getStreet());
            txtHousenumber.setText(shop.getHousenumber());
            txtPostcode.setText(shop.getPostcode());
            txtEmail.setText(shop.getEmail());
            txtWebsite.setText(shop.getWebsite());
            txtPhone.setText(shop.getPhone());
        }
    }

