    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        DB_Access dba = null;
        try {
            dba = new DB_Access();
        } catch (SQLException ex) {
            simple_dialog.infoBox("Fehler bei der Datenbankverbindung!");
        }

        Shop shop = new Shop();
        shop.setId(lShopId);
        shop.setCity(txtCity.getText());
        shop.setEmail(txtEmail.getText());
        shop.setHousenumber(txtHousenumber.getText());

        try {
            shop.setLat(Float.parseFloat(txtLat.getText()));
        } catch (Exception ex) {
            simple_dialog.infoBox("Breitengrad muss numerisch sein!");
            return;
        }

        try {
            shop.setLon(Float.parseFloat(txtLon.getText()));
        } catch (Exception ex) {
            simple_dialog.infoBox("L채ngengrad muss numerisch sein!");
            return;
        }

        shop.setName(txtName.getText());
        shop.setPhone(txtPhone.getText());
        shop.setPostcode(txtPostcode.getText());
        shop.setShop(txtType.getText());
        shop.setStreet(txtStreet.getText());
        shop.setWebsite(txtWebsite.getText());

        if (newShop) {
            // INSERT

            try {
                dba.addShop(shop);
            } catch (SQLException ex) {
                simple_dialog.infoBox("Fehler beim Hinzuf체gen!");
            }

        } else {
            try {
                // UPDATE
                dba.updateShop(shop);
            } catch (SQLException ex) {
                simple_dialog.infoBox("Fehler beim Updaten des Eintrags!");
            }

        }
    }//GEN-LAST:event_btnSaveMouseClicked

